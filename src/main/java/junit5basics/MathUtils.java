package junit5basics;

public class MathUtils {

    public int add(int a, int b) {
        return (a + b);
    }

    public double computeCircleArea(int radius) {
        return 3.14 * radius * radius;
    }

    public int divide(int a, int b) {
        return (a /b);
    }

    public int multiply(int a, int b) {
        return a*b;
    }
}
